#include <stdio.h>
/*
 * <stdio.h> –µ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∞—Ç–∞ –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –∑–∞ –≤—Ö–æ–¥/–∏–∑—Ö–æ–¥. –ë–ª–∞–≥–æ–¥–∞—Ä–µ–Ω–∏–µ –Ω–∞ –Ω–µ—è
 * –∏–º–∞–º–µ —Ñ—É–Ω–∫—Ü–∏–∏ –∫–∞—Ç–æ printf/puts –∏ –º–æ–∂–µ–º –¥–∞ –≤–∏–¥–∏–º —Ä–µ–∑—É–ª—Ç–∞—Ç–∞ –æ—Ç –≤—Å—è–∫–∞ –ø—Ä–æ–º—è–Ω–∞.
 *
 * LessonSandbox –µ –Ω–∞—à–∏—è—Ç ‚Äû–∫–ª–∞—Å‚Äú ‚Äì –≤ C –≥–æ —Ä–µ–∞–ª–∏–∑–∏—Ä–∞–º–µ —Å—ä—Å —Å—Ç—Ä—É–∫—Ç—É—Ä–∞, –∫–æ—è—Ç–æ
 * –ø–∞–∑–∏ —Å—ä—Å—Ç–æ—è–Ω–∏–µ: health –∏ coins.
 */
typedef struct {
    int health;
    int coins;
} LessonSandbox;

static void LessonSandbox_init(LessonSandbox *game) {
    /*
     * LessonSandbox_init –µ –º–µ—Ç–æ–¥—ä—Ç, –∫–æ–π—Ç–æ –∑–∞–¥–∞–≤–∞ –Ω–∞—á–∞–ª–Ω–∏—Ç–µ —Å—Ç–æ–π–Ω–æ—Å—Ç–∏.
     * –¢—É–∫ —Ä–∞–±–æ—Ç–∏–º —Å —Ü–µ–ª–∏ —á–∏—Å–ª–∞ (int). –ü—Ä–æ–º–µ–Ω—è–π —Å–º–µ–ª–æ –∏ –≥–ª–µ–¥–∞–π –∏–∑—Ö–æ–¥–∞.
     *
     * –û–ë–Ø–°–ù–ï–ù–ò–ï –ó–ê *game:
     * - *game –æ–∑–Ω–∞—á–∞–≤–∞, —á–µ game –µ –£–ö–ê–ó–ê–¢–ï–õ (pointer) –∫—ä–º —Å—Ç—Ä—É–∫—Ç—É—Ä–∞ LessonSandbox
     * - * –≤ —Ç–∏–ø–∞ –Ω–∞ –ø–∞—Ä–∞–º–µ—Ç—ä—Ä–∞ (LessonSandbox *game) –¥–µ–∫–ª–∞—Ä–∏—Ä–∞ —É–∫–∞–∑–∞—Ç–µ–ª
     * - –ó–∞—â–æ —É–∫–∞–∑–∞—Ç–µ–ª? –ó–∞ –¥–∞ –º–æ–∂–µ —Ñ—É–Ω–∫—Ü–∏—è—Ç–∞ –¥–∞ –ü–†–û–ú–ï–ù–Ø –æ—Ä–∏–≥–∏–Ω–∞–ª–Ω–∞—Ç–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä–∞,
     *   –∞ –Ω–µ –¥–∞ —Ä–∞–±–æ—Ç–∏ —Å –∫–æ–ø–∏–µ. –ë–µ–∑ * –ø—Ä–æ–º–µ–Ω–∏—Ç–µ –Ω—è–º–∞ –¥–∞ —Å–µ –∑–∞–ø–∞–∑—è—Ç –∏–∑–≤—ä–Ω —Ñ—É–Ω–∫—Ü–∏—è—Ç–∞.
     * - game->health –æ–∑–Ω–∞—á–∞–≤–∞: "–¥–æ—Å—Ç—ä–ø–∏ –ø–æ–ª–µ—Ç–æ health –Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä–∞—Ç–∞, –∫—ä–º –∫–æ—è—Ç–æ —Å–æ—á–∏ game"
     * - –í main() —Å–µ –∏–∑–ø–æ–ª–∑–≤–∞ &sandbox - & –≤—Ä—ä—â–∞ –ê–î–†–ï–°–ê –Ω–∞ –ø—Ä–æ–º–µ–Ω–ª–∏–≤–∞—Ç–∞
     */
    game->health = 60;
    game->coins = 3;
}

static void LessonSandbox_run(LessonSandbox *game) {
    puts("=== Sandbox: –¢–∏–ø–æ–≤–µ, –æ–ø–µ—Ä–∞—Ç–æ—Ä–∏ –∏ if/else ===");

    /*
     * –°—Ç—ä–ø–∫–∞ 1 ‚Äì —Ç–∏–ø–æ–≤–µ –¥–∞–Ω–Ω–∏.
     * –°—ä–∑–¥–∞–π int, float –∏ char, –ø–æ—Å–ª–µ –≥–∏ –æ—Ç–ø–µ—á–∞—Ç–∞–π.
     * –¢–µ—Å—Ç–≤–∞–π —Å–ª–µ–¥ –ø—Ä–æ–º—è–Ω–∞ —Å:
     *   clang Sandbox/training.c -o Sandbox/training && ./Sandbox/training
     */
    int playerLevel = 1;
    float mathScore = 5.50f;
    char favoriteLetter = 'C';
    printf("[–°—Ç—ä–ø–∫–∞ 1] –ù–∏–≤–æ=%d | –£—Å–ø–µ—Ö=%.2f | –ë—É–∫–≤–∞=%c\n",
           playerLevel, mathScore, favoriteLetter);

    /*
     * –°—Ç—ä–ø–∫–∞ 2 ‚Äì –∞—Ä–∏—Ç–º–µ—Ç–∏—á–Ω–∏ –æ–ø–µ—Ä–∞—Ç–æ—Ä–∏.
     * –î–µ–º–æ–Ω—Å—Ç—Ä–∏—Ä–∞–º–µ +, -, * –∏ % –º–µ–∂–¥—É –ª–æ–∫–∞–ª–Ω–∏ –ø—Ä–æ–º–µ–Ω–ª–∏–≤–∏ –∏ —Å—Ç–æ–π–Ω–æ—Å—Ç–∏—Ç–µ –≤ —Å—Ç—Ä—É–∫—Ç—É—Ä–∞—Ç–∞.
     * –£–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ: –ø—Ä–æ–º–µ–Ω–∏ —Ñ–æ—Ä–º—É–ª–∏—Ç–µ –∏ –≤–∏–∂ –Ω–æ–≤–∏—è –∏–∑—Ö–æ–¥.
     */
    int sum = playerLevel + game->coins;
    int difference = game->health - playerLevel;
    int product = game->coins * 2;
    int remainder = game->health % 7;
    printf("[–°—Ç—ä–ø–∫–∞ 2] –°–±–æ—Ä=%d | –†–∞–∑–ª–∏–∫–∞=%d | –ü—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ=%d | –û—Å—Ç–∞—Ç—ä–∫=%d\n",
           sum, difference, product, remainder);

    /*
     * –°—Ç—ä–ø–∫–∞ 3 ‚Äì –æ–ø–µ—Ä–∞—Ç–æ—Ä–∏ –∑–∞ –ø—Ä–∏—Å–≤–æ—è–≤–∞–Ω–µ.
     * –ò–∑–ø–æ–ª–∑–≤–∞–º–µ += –∏ -=, –∑–∞ –¥–∞ –æ–±–Ω–æ–≤–∏–º —Å—Ç–æ–π–Ω–æ—Å—Ç–∏—Ç–µ –∏ –≤–µ–¥–Ω–∞–≥–∞ –≥–∏ –ø—Ä–∏–Ω—Ç–∏—Ä–∞–º–µ.
     * –£–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ: —Å–º–µ–Ω–∏ +5/-1 —Å –¥—Ä—É–≥–∏ —Å—Ç–æ–π–Ω–æ—Å—Ç–∏ –∏ –ø–æ–≤—Ç–æ—Ä–∏ —Ç–µ—Å—Ç–∞.
     */
    game->health += 5;
    game->coins -= 1;
    printf("[–°—Ç—ä–ø–∫–∞ 3] –°–ª–µ–¥ +=/-= -> health=%d | coins=%d\n",
           game->health, game->coins);

    /*
     * –°—Ç—ä–ø–∫–∞ 4 ‚Äì if/else (–µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–∞—Ç–∞ –∫–æ–Ω—Ç—Ä–æ–ª–Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä–∞ –∑–∞—Å–µ–≥–∞).
     * –ê–∫–æ health >= 65, —Å—ä–æ–±—â–∞–≤–∞–º–µ, —á–µ –≥–µ—Ä–æ—è—Ç –µ –≤ –¥–æ–±—Ä–∞ —Ñ–æ—Ä–º–∞, –∏–Ω–∞—á–µ ‚Äì —á–µ –∏–º–∞ –Ω—É–∂–¥–∞ –æ—Ç –ø–æ—á–∏–≤–∫–∞.
     * –£–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ: –ø—Ä–æ–º–µ–Ω–∏ –ø—Ä–∞–≥–∞ –∏–ª–∏ –Ω–∞—á–∞–ª–Ω–∏—è health –∏ –Ω–∞–±–ª—é–¥–∞–≤–∞–π —Ä–∞–∑–ª–∏—á–Ω–∏—Ç–µ —Å—ä–æ–±—â–µ–Ω–∏—è.
     */
    if (game->health >= 65) {
        puts("[–°—Ç—ä–ø–∫–∞ 4] ‚úÖ –ì–µ—Ä–æ—è—Ç –µ –≤ –¥–æ–±—Ä–∞ —Ñ–æ—Ä–º–∞.");
    } else {
        puts("[–°—Ç—ä–ø–∫–∞ 4] ‚ö†Ô∏è –ì–µ—Ä–æ—è—Ç —Ç—Ä—è–±–≤–∞ –¥–∞ —Å–∏ –ø–æ—á–∏–Ω–µ.");
    }

    puts("=== –ö—Ä–∞–π ===");
    puts("üëâ –ü—Ä–æ–¥—ä–ª–∂–∞–≤–∞–π –¥–∞ –ø—Ä–æ–º–µ–Ω—è—à —Å—Ç–æ–π–Ω–æ—Å—Ç–∏ –∏ –¥–∞ –∫–æ–º–ø–∏–ª–∏—Ä–∞—à —Å–ª–µ–¥ –≤—Å—è–∫–∞ —Å—Ç—ä–ø–∫–∞!");
}

int main(void) {
    /* main –µ –≤—Ö–æ–¥–Ω–∞—Ç–∞ —Ç–æ—á–∫–∞ ‚Äì —Å—ä–∑–¥–∞–≤–∞–º–µ ‚Äû–∫–ª–∞—Å–∞‚Äú –∏ –∏–∑–≤–∏–∫–≤–∞–º–µ –º–µ—Ç–æ–¥–∏—Ç–µ –º—É. */
    LessonSandbox sandbox;
    LessonSandbox_init(&sandbox);
    LessonSandbox_run(&sandbox);
    return 0;
}

