/*
 * Проста програма за изчисляване на среден успех
 * 
 * Тази версия не използва масиви - само сумиране "на момента"
 * Подходяща за първи стъпки в програмирането
 */

#include <stdio.h>

int main(void)
{
    int n;              /* Брой ученици */
    int i;              /* Брояч */
    double grade;       /* Текуща оценка */
    double sum = 0.0;  /* Сума на оценките */
    double average;     /* Среден успех */

    /* Въвеждане на броя ученици */
    printf("Колко ученици? ");
    scanf("%d", &n);

    /* Проверка за валиден брой */
    if (n <= 0) {
        printf("Броят трябва да е положително число!\n");
        return 1;
    }

    /* Четене на оценките една по една и сумиране */
    printf("Въведете %d оценки:\n", n);
    for (i = 1; i <= n; i++) {
        printf("  Оценка %d: ", i);
        scanf("%lf", &grade);
        sum = sum + grade;  /* или: sum += grade; */
    }

    /* Изчисляване на средния успех */
    average = sum / n;

    /* Резултат */
    printf("\n--- Резултат ---\n");
    printf("Среден успех: %.2f\n", average);

    return 0;
}
