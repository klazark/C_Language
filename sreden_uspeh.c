/*
 * Програма за изчисляване на среден успех на ученици
 * 
 * Тази програма:
 * 1. Пита потребителя за броя на учениците
 * 2. Чете оценките на всеки ученик
 * 3. Изчислява средния успех (аритметично средно)
 * 4. Показва резултата
 */

#include <stdio.h>

#define MAX_STUDENTS 100  /* Максимален брой ученици */

int main(void)
{
    int n;                          /* Брой ученици */
    double grades[MAX_STUDENTS];   /* Масив за съхранение на оценките */
    double sum = 0.0;              /* Сума на всички оценки */
    double average;                /* Среден успех */
    int i;                         /* Брояч за цикли */

    /* Въвеждане на броя ученици */
    printf("Въведете брой ученици (1-%d): ", MAX_STUDENTS);
    if (scanf("%d", &n) != 1) {
        printf("Грешка: Невалиден вход.\n");
        return 1;
    }

    /* Проверка дали броят е валиден */
    if (n <= 0 || n > MAX_STUDENTS) {
        printf("Грешка: Броят ученици трябва да е между 1 и %d.\n", MAX_STUDENTS);
        return 1;
    }

    /* Въвеждане на оценките */
    printf("\nВъведете оценките (2.00 до 6.00):\n");
    for (i = 0; i < n; i++) {
        printf("  Оценка на ученик %d: ", i + 1);
        if (scanf("%lf", &grades[i]) != 1) {
            printf("Грешка: Невалидна оценка.\n");
            return 1;
        }

        /* Проверка дали оценката е в валиден диапазон */
        if (grades[i] < 2.00 || grades[i] > 6.00) {
            printf("Грешка: Оценката трябва да е между 2.00 и 6.00.\n");
            return 1;
        }

        /* Добавяме оценката към сумата */
        sum += grades[i];
    }

    /* Изчисляване на средния успех */
    average = sum / n;

    /* Извеждане на резултата */
    printf("\n=================================\n");
    printf("Брой ученици: %d\n", n);
    printf("Сума на оценките: %.2f\n", sum);
    printf("Среден успех: %.2f\n", average);
    printf("=================================\n");

    /* Оценка на средния успех */
    if (average >= 5.50) {
        printf("Отличен среден успех!\n");
    } else if (average >= 4.50) {
        printf("Много добър среден успех!\n");
    } else if (average >= 3.50) {
        printf("Добър среден успех.\n");
    } else if (average >= 3.00) {
        printf("Среден успех.\n");
    } else {
        printf("Трябва да се постараете повече.\n");
    }

    return 0;
}
