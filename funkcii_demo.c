#include <stdio.h>

/* ============================================================
   ДЕМО ПРОГРАМА - Функции в C
   Покрива: дефиниция, извикване, параметри по стойност,
            глобални/локални променливи, указатели, прототипи
   ============================================================ */


/* ----------------------------------------------------------
   ГЛОБАЛНИ (външни) променливи - видими навсякъде в програмата
   ---------------------------------------------------------- */
int global_x = 12;
int global_y = 10;


/* ----------------------------------------------------------
   ПРОТОТИПИ (декларации) на функциите - казваме на компилатора
   какви функции ще използваме, преди да ги дефинираме
   ---------------------------------------------------------- */
float stepen(float a, int n);
void  fig(int n, char c);
void  change_stойност(int a, int b);
void  change_globalni();
void  change_ukazatel(int *pa, int *pb);
int   uravn(float a, float b, float *px);


/* ----------------------------------------------------------
   1. ФУНКЦИЯ ЗА СТЕПЕН
      Връща: float (дробно число)
      Параметри: a - основа, n - показател
   ---------------------------------------------------------- */
float stepen(float a, int n)
{
    float P = 1;
    int m, i;

    if (n > 0) m = n; else m = -n;

    for (i = 1; i <= m; i++)
        P = P * a;

    if (n < 0) P = 1 / P;

    return P;
}


/* ----------------------------------------------------------
   2. ФУНКЦИЯ ЗА ТРИЪГЪЛНА ФИГУРА
      Връща: void (нищо не връща)
      Параметри: n - брой редове, c - символ
   ---------------------------------------------------------- */
void fig(int n, char c)
{
    int i, k;
    for (i = 1; i <= n; i++)
    {
        for (k = 1; k <= i; k++)
            printf("%2c", c);
        printf("\n");
    }
}


/* ----------------------------------------------------------
   3. РАЗМЯНА ПО СТОЙНОСТ - НЕ РАБОТИ за оригиналните x и y!
      Функцията получава КОПИЯ на x и y, не оригиналите.
   ---------------------------------------------------------- */
void change_stойност(int a, int b)
{
    int c;
    c = a;
    a = b;
    b = c;
    /* a и b са само копия - x и y в main() НЕ се променят */
}


/* ----------------------------------------------------------
   4. РАЗМЯНА С ГЛОБАЛНИ ПРОМЕНЛИВИ - работи, но само за
      global_x и global_y. Не може да се използва за друга двойка.
   ---------------------------------------------------------- */
void change_globalni()
{
    int c;          /* локална променлива - видима само тук */
    c = global_x;
    global_x = global_y;
    global_y = c;
}


/* ----------------------------------------------------------
   5. РАЗМЯНА С УКАЗАТЕЛИ - правилното решение!
      Параметрите са АДРЕСИ на променливите, не копия.
      *pa означава "стойността на адреса, към който сочи pa"
   ---------------------------------------------------------- */
void change_ukazatel(int *pa, int *pb)
{
    int c;
    c   = *pa;   /* c = стойността на x */
    *pa = *pb;   /* x = стойността на y */
    *pb = c;     /* y = старата стойност на x */
}


/* ----------------------------------------------------------
   6. ФУНКЦИЯ ЗА ЛИНЕЙНО УРАВНЕНИЕ ax + b = 0
      Връща: 1 = безброй решения, 2 = няма решение, 3 = едно решение
      *px е указател - чрез него записваме резултата x навън
   ---------------------------------------------------------- */
int uravn(float a, float b, float *px)
{
    if (a == 0)
    {
        if (b == 0) return 1;   /* 0*x + 0 = 0 -> вярно за всяко x */
        else        return 2;   /* 0*x + 5 = 0 -> невъзможно */
    }
    else
    {
        *px = -b / a;           /* записваме решението директно в x */
        return 3;
    }
}


/* ----------------------------------------------------------
   ГЛАВНА ФУНКЦИЯ - тук започва изпълнението на програмата
   ---------------------------------------------------------- */
int main()
{
    /* --- Демо 1: Степен --- */
    printf("=== 1. ФУНКЦИЯ ЗА СТЕПЕН ===\n");
    float a, b, s;
    int n, k;

    a = 2.0f; n = 3;
    b = 3.0f; k = 2;

    s = stepen(a, n) + stepen(b, k);
    printf("a=%.1f, n=%d, b=%.1f, k=%d\n", a, n, b, k);
    printf("a^n + b^k = %.1f + %.1f = %.1f\n\n", stepen(a, n), stepen(b, k), s);


    /* --- Демо 2: Триъгълна фигура --- */
    printf("=== 2. ТРИЪГЪЛНА ФИГУРА (5 реда от '*') ===\n");
    fig(5, '*');
    printf("\n");


    /* --- Демо 3: Размяна по стойност (НЕ работи) --- */
    printf("=== 3. РАЗМЯНА ПО СТОЙНОСТ (НЕ работи) ===\n");
    int x = 12, y = 10;
    printf("Преди: x=%d  y=%d\n", x, y);
    change_stойност(x, y);
    printf("След:  x=%d  y=%d  (НЕ са разменени!)\n\n", x, y);


    /* --- Демо 4: Размяна с глобални променливи --- */
    printf("=== 4. РАЗМЯНА С ГЛОБАЛНИ ПРОМЕНЛИВИ ===\n");
    printf("Преди: global_x=%d  global_y=%d\n", global_x, global_y);
    change_globalni();
    printf("След:  global_x=%d  global_y=%d  (разменени!)\n\n", global_x, global_y);


    /* --- Демо 5: Размяна с указатели (правилното решение) --- */
    printf("=== 5. РАЗМЯНА С УКАЗАТЕЛИ ===\n");
    int p = 7, q = 99;
    printf("Преди: p=%d  q=%d\n", p, q);
    change_ukazatel(&p, &q);    /* предаваме АДРЕСИТЕ с & */
    printf("След:  p=%d  q=%d  (разменени!)\n\n", p, q);


    /* --- Демо 6: Линейно уравнение --- */
    printf("=== 6. ЛИНЕЙНО УРАВНЕНИЕ ax + b = 0 ===\n");
    float koef_a, koef_b, resh_x;
    int rezultat;

    /* Случай 1: 2x + (-6) = 0  ->  x = 3 */
    koef_a = 2.0f; koef_b = -6.0f;
    rezultat = uravn(koef_a, koef_b, &resh_x);
    printf("%.1fx + (%.1f) = 0  ->  ", koef_a, koef_b);
    switch (rezultat)
    {
        case 1: printf("Вярно за всяко x\n"); break;
        case 2: printf("Няма решение\n");     break;
        case 3: printf("x = %.2f\n", resh_x); break;
    }

    /* Случай 2: 0x + 5 = 0  ->  няма решение */
    koef_a = 0.0f; koef_b = 5.0f;
    rezultat = uravn(koef_a, koef_b, &resh_x);
    printf("%.1fx + %.1f = 0  ->  ", koef_a, koef_b);
    switch (rezultat)
    {
        case 1: printf("Вярно за всяко x\n"); break;
        case 2: printf("Няма решение\n");     break;
        case 3: printf("x = %.2f\n", resh_x); break;
    }

    /* Случай 3: 0x + 0 = 0  ->  безброй решения */
    koef_a = 0.0f; koef_b = 0.0f;
    rezultat = uravn(koef_a, koef_b, &resh_x);
    printf("%.1fx + %.1f = 0  ->  ", koef_a, koef_b);
    switch (rezultat)
    {
        case 1: printf("Вярно за всяко x\n"); break;
        case 2: printf("Няма решение\n");     break;
        case 3: printf("x = %.2f\n", resh_x); break;
    }

    return 0;
}
